#include <iostream>
using namespace std;

int main(){
int X=5;
cout<<"Increment ++x"<<endl;
cout<<"X = "<<X<<endl;
cout<<"++X ="<<++X<<endl;
cout<<"X ="<<X<<endl;


X=5;


cout<<"Increment x++"<<endl;
cout<<"X ="<<X<<endl;
cout<<"X++ ="<<X++<<endl;
cout<<"X ="<<X<<endl;
X=5;



cout<<"Decrement --x"<<endl;
cout<<"X ="<<X<<endl;
cout<<"--X ="<<--X<<endl;
cout<<"X ="<<X<<endl;
X=5;


cout<<"Decrement x--"<<endl;
cout<<"X ="<<X<<endl;
cout<<"X-- ="<<X--<<endl;
cout<<"X ="<<X<<endl;
X=5;
return 0;
}
